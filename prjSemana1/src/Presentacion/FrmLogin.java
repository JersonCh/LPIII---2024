    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
         //dni clave
        clsEDocente objEd=new clsEDocente();
        objEd.setDni(txtUsuario.getText());
        objEd.setClave(Integer.parseInt(TxtClave.getText()));        
        clsNDocente objNd=new clsNDocente();
         ResultSet rs=objNd.MtdValidarLogin(objEd);
         int contador=0;
         try {
            while (rs.next())
            {
                //si no existe mensaje de error
                contador++;                
            }
            if(contador>=1)
                {
                    FrmNota frm=new FrmNota();
                    frm.show();
                    this.hide();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ERROR");
                }
        } catch (SQLException e) {
        }
            System.out.println("El dni del docente es: "+objEd.getDni());
    }//GEN-LAST:event_btnAceptarActionPerformed
    
